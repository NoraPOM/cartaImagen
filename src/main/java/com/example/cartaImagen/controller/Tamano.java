package com.example.cartaImagen.controller;

public class Tamano {
    Integer alto;
    Integer ancho;
    public Tamano(Integer alto, Integer ancho) {
        this.alto = alto;
        this.ancho = ancho;
    }
    public Integer getAlto() {
        return alto;
    }
    public void setAlto(Integer alto) {
        this.alto = alto;
    }
    public Integer getAncho() {
        return ancho;
    }
    public void setAncho(Integer ancho) {
        this.ancho = ancho;
    }
    
    
}
